#include<iostream>
using namespace std;

int main(){
    int N,M;
    cin>>N>>M;

    cout<<N<<" + "<<M<<" = "<<N+M<<endl;
    cout<<N<<" - "<<M<<" = "<<N-M<<endl;
    cout<<N<<" * "<<M<<" = "<<N*M<<endl;
    cout<<N<<" / "<<M<<" = "<<N/M<<endl;
    cout<<N<<" % "<<M<<" = "<<N%M<<endl;

    return 0;
}